def remove_bourbon_from_collection(collection_id, bourbon_id):
    collections = Collection.get_all()
    if collections:
        cprint("\n ---Select a Collection---", 'blue', attrs=['bold'])
        for collection in collections:
            print(f"\nID: {collection.id}, Name: {collection.name}")
        collection_id = int(input("\nEnter the ID of the collection: "))
        collection = next((c for c in collections if c.id == collection_id), None)
        if collection:
            bourbon = session.query(Bourbon).filter_by(id=bourbon_id).first()
            if bourbon:
                collection.bourbons.remove(bourbon)
                session.commit()
                print("\nBourbon removed from collection successfully!")
            else:
                print("\nBourbon not found.")
        else:
            print("\nCollection not found.")
